    @staticmethod
    def _is_prediction_insight_query(lowered: str) -> bool:
        # Match explicit insight questions
        if (CopilotService._contains_any(lowered, ["prediction", "this prediction"])
            and CopilotService._contains_any(lowered, ["indicates", "importance", "conclusion", "suggestion", "what does", "explain"])):
            return True
        
        # Match questions about predicted value
        if CopilotService._contains_any(lowered, ["predicted value", "prediction value", "this value", "this predicted"]):
            return True
        
        # Match "what is" + prediction-related terms
        if "what is" in lowered and CopilotService._contains_any(lowered, ["prediction", "predicted", "value", "result"]):
            return True
        
        # Match "explain" + any prediction term
        if "explain" in lowered and CopilotService._contains_any(lowered, ["prediction", "predicted", "value", "result", "this"]):
            return True
        
        # Match "what does" + "mean" or similar
        if CopilotService._contains_any(lowered, ["what does", "what's", "whats"]) and CopilotService._contains_any(lowered, ["mean", "indicate", "suggest"]):
            return True
        
        return False

