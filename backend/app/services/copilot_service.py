    @staticmethod
    def _is_greeting_or_help_query(lowered: str, message: str) -> bool:
        """Detect if user is greeting, asking for help, or just opened copilot."""
        # Empty or very short messages
        if len(message.strip()) == 0 or len(message.strip()) < 3:
            return True
        
        greeting_terms = ["hi", "hello", "hey", "help", "what can you do", "how can you help", 
                         "what can i ask", "guide me", "start", "intro", "greet"]
        return any(term in lowered for term in greeting_terms) and len(message.split()) <= 5

